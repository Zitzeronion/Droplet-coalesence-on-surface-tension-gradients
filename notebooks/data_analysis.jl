# ╔═╡ 481914ab-9e8b-4059-8ea3-c39429fe8695
md"## Coalescence of sessile droplets

We study the coalescence dynamics of two liquid droplets.
The droplets are placed on top of a rigid substrate with a small contact angle. 
Their overlap is minimal meaning there is just a tiny liquid bridge between them.
Following the relatively simple argumentation of surface energy minimization, one expects that the bridge quickly grows and that the two droplets coalesce into a single one.
This is indeed what happens when to droplets of the same liquid come into contact with each other.
Many people have approached this problem for various resons  [[1](https://www.cambridge.org/core/services/aop-cambridge-core/content/view/0DE6817B486607109F5A32018951B5C0/S002211209900662Xa.pdf/coalescence_of_liquid_drops.pdf),[2](https://www.cambridge.org/core/services/aop-cambridge-core/content/view/CB56EE5EDDAFF2E9503CFEB0D2110662/S0022112003004646a.pdf/div-class-title-inviscid-coalescence-of-drops-div.pdf),[3](https://journals.aps.org/prl/pdf/10.1103/PhysRevLett.95.164503?casa_token=kdvf_DF7sK0AAAAA%3AN1Kw_LdxZMAfmGjHF5Cj39M7tugTp1frIl7HWV32tQw19igd43ha4Sk18H6fpJf2ZHruWHmQWK48),[4](https://arxiv.org/pdf/1406.5842.pdf),[5](https://journals.aps.org/prl/pdf/10.1103/PhysRevLett.106.114501?casa_token=rMfqSWjkqFUAAAAA%3AHWLZ6mGKdkI5nZj2PUNaHMDsnL_33bqD36iRgScHUpDQM-sIavUNOkwRobjobvm8fOQ9CStgJB_6)], mostly to understand the coalescence of freely suspended droplets. 
Sessile droplets on the other hand were studied in [[6](https://aip.scitation.org/doi/abs/10.1063/1.4824108?showFTTab=true&journalCode=phf),[7](https://journals.aps.org/prl/pdf/10.1103/PhysRevLett.111.144502?casa_token=I4xo5sbhnq4AAAAA%3A89YP7q4bHs2kJ36AKKgWzhrXRdCdNAUgcbQdXkEWFNHD_29yORY51rqk1na3HLToTi3xuBW4KWyp)] or more so their coalescence on partially wetting substrates with contact angles below $\pi/2$.
We try to model the later, sessile droplets on a partially wetting substrate.
In this case we know that the bridge connecting ($h_0(t)$) them should grow with 

$h_0(t) \propto t^{2/3}.$

We're going to take this case as a starting point for the following experiments.
We further introduce a surface tension gradient, mimicing an effective Marangoni flow.
This gradient can for example be introduced with the addition of surfactants. 
Therefore putting surfactants only in one of the two droplets.
Not with surfactants but with different but miscible liquids one observes that this gradient stabilizes a twin droplet state.
Riegler and Lazaro [[8](https://pubs.acs.org/doi/abs/10.1021/la800630w)] first and later by Karpitschka et al. [[9](https://journals.aps.org/prl/abstract/10.1103/PhysRevLett.109.066103), [10](https://pubs.acs.org/doi/abs/10.1021/la500459v), [11](https://www.cambridge.org/core/journals/journal-of-fluid-mechanics/article/abs/sharp-transition-between-coalescence-and-noncoalescence-of-sessile-drops/121D63B9ABFD9040C327ED8AC10FBE01)] have shown that the powerlaw does no longer look like the one above but

$h_0(t) \propto h_0(0).$

Meaning that the droplets do not coalesce.
Instead however the whole twin-droplet system is moving with a small but measurable velocity.
This actually happens when the surface tension gradient is rather steep or sharp.
What we will now discuss is the case in between those boundaries.

- No gradient : $h_0(t) \propto t^{2/3}$ 
- A sharp gradient: $h_0(t) = const$ 
- A smooth gradient: $h_0(t) = ?$

## Numerical setup

Simulations or numerical experiments are performed using the solver *[Swalbe.jl](https://github.com/Zitzeronion/Swalbe.jl)* (using the surface_tension_gradient branch).
As of now these simulations are work in progress and surface tension gardients are not yet part of the master branch.
Understand this as a note of caution, this is not a laboratory experiment!


