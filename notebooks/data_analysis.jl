# ╔═╡ 3f55eb00-33ef-46df-bb20-a539f41bd31b
"""
    run_gamma_periodic(sys, gamma; r₁=115, r₂=115, θ₀=1/9, verbos=true, dump = 100, fluid=zeros(sys.param.Tmax÷dump, sys.L))

Simulation of coalescing droplets on a bounded domain
"""
function run_gamma_periodic(
    sys::Swalbe.SysConst_1D,
    gamma::Vector;
    r₁=115,
    r₂=115, 
    θ₀=1/9,
	drop_cent=(sys.L/3, 2*sys.L/3),  
    verbos=true, 
    dump = 100, 
    fluid=zeros(sys.param.Tmax÷dump, sys.L)
)
    println("Simulating droplet coalecense with surface tension gardient on a periodic domain")
    state = Swalbe.Sys(sys, kind="gamma")
    state.basestate.height .= Swalbe.two_droplets(sys, r₁=r₁, r₂=r₂, θ₁=θ₀, θ₂=θ₀, center=drop_cent)
    Swalbe.equilibrium!(state, sys)
    state.γ .= gamma
	Swalbe.∇γ!(state)
	state.∇γ[L-4:L] .= 0
	state.∇γ[1:4] .= 0
    println("Starting the lattice Boltzmann time loop")
    for t in 1:sys.param.Tmax
        if verbos
            if t % sys.param.tdump == 0
                mass = 0.0
                mass = round(sum(state.basestate.height), digits=3)
				if 0.0 / mass ≠ 0.0
					println("Something went wrong, the mass is likely not a number")
					break
				end
                println("Time step $t bridge height is $(round(minimum(state.basestate.height[sys.L÷2-50:sys.L÷2+50]), digits=3)) and total mass $(mass)")
            end
        end
        Swalbe.filmpressure!(state, sys, γ=gamma)
        Swalbe.h∇p!(state)
        Swalbe.slippage!(state, sys)
        state.basestate.F .= -state.basestate.h∇p .- state.basestate.slip .+ state.∇γ
        Swalbe.equilibrium!(state, sys)
        Swalbe.BGKandStream!(state, sys)
        Swalbe.moments!(state)
        
        Swalbe.snapshot!(fluid, state.basestate.height, t, dumping = dump)
    end
	
    return fluid
    
end

# ╔═╡ 88dc323f-f640-4bd8-9db8-7965785663e2
md"This functions generates a single data set. 
What is missing is an iteration over different surface tension fields.
The concrete choice for the spatially resolved surface tension is given by either one of three functions,

$\gamma^{const.}(x) = \gamma_0$

where γ₀ is a characteristic surface tension value. 
Besides the constant surface tension, we use a model given by

$\gamma^{heavi}(x) = \begin{cases}\gamma_0\qquad\qquad\text{for x < L/2}\\ \gamma_0(1-\epsilon)\quad \text{else}\end{cases},$

with a Heaviside step function at the touching point where ϵ is a small deviation and $L$ is the size of the system.
A third option is given, using a tangent hyperbolicus to smoothen out the discontinuity of the Heaviside function.
Therefore, the function reads as

$γ^{smooth}(x) = γ₀[s(x) + (1-s(x))(1-ϵ)],$

with $s(x)$ being

$s(x) = \Bigg|1 - \left[\frac{1}{2} + \frac{1}{2}\tanh\left(\frac{x - a}{b}\right)\right]\Bigg|,$

where $a$ and $b$ define the intersection point and the smoothing width, respectively.
For the rest of this notebook we set 

$a=L/2$ 

and will vary $b$.
"

